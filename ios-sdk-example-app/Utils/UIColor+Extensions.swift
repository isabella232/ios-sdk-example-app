import Foundation
import UIKit

extension UIColor {
  public class func vibesTitleLabel() -> UIColor {
    return UIColor(red:0.13, green:0.13, blue:0.13, alpha:1.0)
  }
  
  public class func vibesValueLabel() -> UIColor {
    return UIColor(red:0.46, green:0.46, blue:0.46, alpha:1.0)
  }
  
  public class func vibesViewBackground() -> UIColor {
    return UIColor(red:0.97, green:0.97, blue:0.97, alpha:1.0)
  }
  
  public class func vibesButtonColor() -> UIColor {
    return UIColor(red:0.14, green:0.48, blue:0.84, alpha:1.0)
  }
  
  public class func vibesTextFieldTextColor() -> UIColor {
    return UIColor(red:0.13, green:0.13, blue:0.13, alpha:1.0)
  }
  
  public class func vibesTextFieldPlaceHolderTextColor() -> UIColor {
    return UIColor(red:0.72, green:0.72, blue:0.72, alpha:1.0)
  }
  
  public class func vibesTextFieldBorderColor() -> UIColor {
    return UIColor(red:0.72, green:0.72, blue:0.72, alpha:1.0)
  }
}
